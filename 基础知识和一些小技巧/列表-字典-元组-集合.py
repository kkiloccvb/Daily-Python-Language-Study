#10、-集合----------------------------------------------------------------------------------------------------
'''
集合不支持索引
空集合用set()创建
集合中元素唯一,自动去重
集合内元素是无序的
集合内数据必须为不可变类型
可变：字典、列表、集合
不可变:int、float、bool、字符串、元组
创建:
(1)set=()
添加:
(1).add()
删除:
(1).pop()
(2).remove() 确信元素必须存在，且不存在即视为错误时使用
(3).discard() 不确定元素是否存在，且只想确保它被移除时使用
'''
