#7、-列表------------------------------------------------------------------------------------------------------
'''
列表[]
列表中元素可以是任何数据类型，包括列表本身
列表中的元素可以通过索引表示,从0开始,负数索引从末端开始
添加：
(1).append 末尾添加数据
(2).insert(a , b) 索引为a后面插b
(3).extend([ , ]) 列表加多个数据
删除：
(1)del li[a] 删除索引a数据
(2).remove(a) 删除数据a
(3).pop(a) 删除指定索引a,并返回删除指定索引a的数据
查询：
(1).index(a) 查询指定数据a,不存在则报错
(2).count(a) 查询指定数据a的个数
排序：
(1).sort() 纯数字排序默认升序
反序：
(1).reverse() 数字进行倒序,不排序
'''
#8-字典-------------------------------------------------------------------------------------------------------
'''
不允许重复
创建：
(1)info={key1:value1,k2:v2} 创建字典
获取：
(1)print(info['k2']) 输出v2
(2)info.keys() 获取字典所有的key
(3)info.values() 获取字典所有的value
(4)info.items() 获取字典所有的key和value
添加：
(1)info[k3]=v3 字典添加 'k3':'v3'
(2)info.update({'k4':'v4','k5':'v5'}) 字典添加'k4':'v4' 'k5':'v5'
删除：
(1)del info['k2'] 删除v2
(2)info.pop('k2') 删除v2
(3)info.popitem() 删除最后一个键值对
(4)info.clear() 清除所有字典
查找：
(1)res = info['k2'] 查找字典k2对应的
(2)res = info.get('k2') 通过get获取value值
(3)res = info.get('k1134','不存在') 当无法查找到k1134,输出不存在
'''
