#19、-类---------------------------------------------------------------------------------------------------------
'''
面向对象编程按类和对象封装：
面向对象编程特征：
(1)封装:将数据和操作数据的方法封装到对象内部
(2)继承:子类继承父类的属性和方法,可以扩展和重写这些方法
(3)多态:不同对象对同一方法有着不同的实现
对象通过类来创建
定义类的语法:
class 类名：
    类的属性和方法
(属性):描述对象的特征和性质
    类属性:对于整个类的变量,所有实例都共享
        公有属性:定义在类里面的变量,通过类直接访问
        私有属性:类里面双下滑(__)先开头的变量,在类外部私有数据不能访问
    类实例属性:特定于某个对象的变量,在构造方法中定义
(方法):描述对象的行为或功能
    实例方法:作用于实例对象的方法
    类方法:作用于类本身的方法，要用@classmethod装饰器
    静态方法:独立于类和实例的方法,不访问类属性和实例属性,要用@staticmethod装饰器

'''